const PH_I18n = {
  lang: "es",

  init() {
    const saved = localStorage.getItem(PH_LOCALE_KEY);
    this.lang = saved === "en" ? "en" : "es";
    document.documentElement.lang = this.lang;
  },

  getLocale() {
    return this.lang === "en" ? "en-US" : "es-DO";
  },

  t(path) {
    const keys = path.split(".");
    let node = PH_TRANSLATIONS[this.lang];
    for (const key of keys) {
      if (node == null) return path;
      node = node[key];
    }
    return node ?? path;
  },

  setLang(lang) {
    if (lang !== "es" && lang !== "en") return;
    this.lang = lang;
    localStorage.setItem(PH_LOCALE_KEY, lang);
    document.documentElement.lang = lang;
    if (typeof PH_onLanguageChange === "function") PH_onLanguageChange();
  },

  months(count = 7) {
    return PH_TRANSLATIONS[this.lang].common.monthsShort.slice(0, count);
  },

  category(key) {
    return this.t(`common.categories.${key}`);
  },

  segment(key) {
    return this.t(`common.segments.${key}`);
  },

  acquisition(key) {
    return this.t(`common.acquisition.${key}`);
  },

  engagement(key) {
    return this.t(`common.engagement.${key}`);
  },

  city(key) {
    return this.t(`common.cities.${key}`);
  },

  expansionStatus(key) {
    return this.t(`common.expansionStatus.${key}`);
  },

  scenario(key) {
    return this.t(`common.scenarios.${key}`);
  },
};

PH_I18n.init();
